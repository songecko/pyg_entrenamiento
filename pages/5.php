					<h2>HISTORIA DE P&G </h2>
                    <p>&bull; <span class="textoDestacado">En 1837</span>, la empresa Procter & Gamble fué fundada en Cincinnati, Ohio, Estados Unidos, por William Procter, un inglés fabricante de velas y por James Gamble un irlandés fabricante de jabón.</p>
                    <p>&bull; <span class="textoDestacado">En 1948</span>, comenzaron las operaciones de P&G en México, primer ingreso internacional después de la Segunda Guerra Mundial y primera entrada en Latinoamérica.</p>
                    <p>&bull; <span class="textoDestacado">En 2012</span>, P&G es la empresa líder en fabricantes de productos de consumo con más de 115 millones de consumidores en México y con liderazgo en la mayoría de las marcas.</p>
                    <p>&bull; <span class="textoDestacado">En México</span>, al día de hoy se cuenta con alrededor de 7,000 empleados trabajando en forma directa en 9 plantas, un centro de distribución y oficinas generales, generando una de las historias de éxito más sólidas en el mundo de P&G. </p>
